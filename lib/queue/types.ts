/** Returns expiresAt timestamp for a channel, N hours from now. */
export function calculateExpiresAt(channel: QueueChannel, fromDate = new Date()): string {
  const policy = CHANNEL_POLICIES[channel];
  const expires = new Date(fromDate.getTime() + policy.expiryHours * 60 * 60 * 1000);
  return expires.toISOString();
}
